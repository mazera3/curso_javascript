// Aula04 - objetos {key:value}

let pessoa = { 
    nome:'Édio',
    idade: 54,
    situacao: true,
    sobrenome: 'Mazera'

}

console.log('Nome', pessoa.nome); // nome -> String -> Edio
console.log('idade', pessoa.idade); // idade -> String -> 54
console.log('situaçao', pessoa.situacao); // situacao -> Boolean -> true
console.log('Sobrenome', pessoa.sobrenome); // Sobrenome -> String -> Mazera
console.log('Objeto', pessoa) // Objeto -> Object { nome: "Édio", idade: 54, situacao: true, sobrenome: "Mazera" }

